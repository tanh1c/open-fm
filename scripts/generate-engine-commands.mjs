#!/usr/bin/env node
//
// generate-engine-commands.mjs — extract AppHandle method signatures from the
// wasm-pack TypeScript bindings and emit a typed COMMANDS map for wasmCore.ts.
//
// Run after every wasm-pack build. The Vite plugin already does this via the
// `npm run build:engine` script in package.json (added by this commit).

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SOURCE = resolve(ROOT, "wasm-pkg-app/openfootmanager_lib.d.ts");
const OUTPUT = resolve(ROOT, "src/core/engineCommands.generated.ts");

// Match: methodName(arg1: type, arg2?: type | null): any;
// Captures method name and the parameter list.
const METHOD_RE = /^\s{4}([a-zA-Z][a-zA-Z0-9]*)\(([^)]*)\):\s*[^;]+;/gm;

const PARAM_NAME_RE = /^\s*([a-zA-Z_][a-zA-Z0-9_]*)\??:/;

function snakeToCamel(name) {
  return name.replace(/_([a-z0-9])/g, (_match, ch) => ch.toUpperCase());
}

function extractParams(paramList) {
  if (!paramList.trim()) return [];
  // Split on top-level commas only. Args here are simple (`x: string`,
  // `y?: number | null`) so a naive split is correct.
  const parts = [];
  let depth = 0;
  let start = 0;
  for (let i = 0; i < paramList.length; i++) {
    const ch = paramList[i];
    if (ch === "<" || ch === "(") depth++;
    else if (ch === ">" || ch === ")") depth--;
    else if (ch === "," && depth === 0) {
      parts.push(paramList.slice(start, i));
      start = i + 1;
    }
  }
  parts.push(paramList.slice(start));

  return parts.map((p) => {
    const match = PARAM_NAME_RE.exec(p);
    if (!match) {
      throw new Error(`Could not parse parameter name from: ${p}`);
    }
    return match[1];
  });
}

function main() {
  const dts = readFileSync(SOURCE, "utf8");
  const commands = {};

  let match;
  METHOD_RE.lastIndex = 0;
  while ((match = METHOD_RE.exec(dts)) !== null) {
    const [, methodName, paramList] = match;
    if (
      methodName === "init" ||
      methodName === "free" ||
      methodName === "constructor"
    ) {
      continue;
    }
    const argNames = extractParams(paramList);
    // Tauri callers pass camelCase keys, so build the map with camelCase keys
    // matching the JS-facing arg names (snake_case from .d.ts → camelCase).
    const camelArgs = argNames.map(snakeToCamel);
    commands[methodName] = camelArgs;
  }

  const sortedNames = Object.keys(commands).sort();
  const lines = [];
  lines.push("// AUTO-GENERATED by scripts/generate-engine-commands.mjs.");
  lines.push("// Do NOT edit by hand. Run `npm run build:engine` to refresh.");
  lines.push("//");
  lines.push("// Maps Tauri-style snake_case command name → AppHandle method + arg key order.");
  lines.push("");
  lines.push("export interface CommandSpec {");
  lines.push("  /** AppHandle method name (camelCase). */");
  lines.push("  method: string;");
  lines.push("  /** Argument keys in the order Rust declared them (camelCase). */");
  lines.push("  args: readonly string[];");
  lines.push("}");
  lines.push("");
  lines.push("export const ENGINE_COMMANDS: Readonly<Record<string, CommandSpec>> = Object.freeze({");

  for (const method of sortedNames) {
    const argList = commands[method].map((a) => `"${a}"`).join(", ");
    // Snake-case the method name for the Tauri command key
    const snakeName = method.replace(/([A-Z])/g, "_$1").toLowerCase();
    lines.push(`  ${snakeName}: { method: "${method}", args: [${argList}] as const },`);
  }
  lines.push("});");
  lines.push("");

  writeFileSync(OUTPUT, lines.join("\n"));
  console.log(`Wrote ${sortedNames.length} commands to ${OUTPUT}`);
}

main();
